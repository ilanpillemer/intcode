function phase(input)
    str2 = ""
    for pos = length(input):-1:1
        str = input[pos:length(input)]
        digit = mod(sum([parse(Int8, x) for x in str]), 10)
        str2 = "$digit$str2"
    end
    str2
end

for i = 1:100
    global input = phase(input)
    println(input[1:8])
